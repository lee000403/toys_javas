
CREATE TABLE ANSWERS
(
  ANSWER_ID   VARCHAR(50) NOT NULL COMMENT '유니크ID',
  ANSWER      VARCHAR(50) NOT NULL COMMENT '답항',
  QUESTION_ID VARCHAR(50) NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (ANSWER_ID)
) COMMENT '답항';

CREATE TABLE QUESTIONS
(
  QUESTION_ID VARCHAR(50)  NOT NULL COMMENT '유니크ID',
  QUESTION    VARCHAR(500) NOT NULL COMMENT '문항',
  PRIMARY KEY (QUESTION_ID)
) COMMENT '문항';

CREATE TABLE Statistics
(
  STATISTICS_ID VARCHAR(50) NOT NULL COMMENT '유니크ID',
  USER_ID       VARCHAR(50) NOT NULL COMMENT '유니크ID',
  QUESTION_ID   VARCHAR(50) NOT NULL COMMENT '유니크ID',
  ANSWER_ID     VARCHAR(50) NOT NULL COMMENT '유니크ID',
  PRIMARY KEY (STATISTICS_ID)
) COMMENT '통계';

CREATE TABLE USERS
(
  USER_ID VARCHAR(50) NOT NULL COMMENT '유니크ID',
  USER    VARCHAR(50) NOT NULL COMMENT '이름',
  GENDER  VARCHAR(50) NOT NULL COMMENT '성별',
  PRIMARY KEY (USER_ID)
) COMMENT '설문자';

